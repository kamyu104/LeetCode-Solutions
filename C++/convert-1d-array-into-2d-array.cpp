// Time:  O(m * n)
// Space: O(1)

class Solution {
public:
    vector<vector<int>> construct2DArray(vector<int>& original, int m, int n) {
        if (size(original) != m * n) {
            return {};
        }
        vector<vector<int>> result;
        for (int i = 0; i < size(original); i += n) {
            result.emplace_back(cbegin(original) + i, cbegin(original) + i + n);
        }
        return result;
    }
};
